package com.dicoding.javafundamental.perulangan;

public class coba {
    public static void main(String[] args){
        int kamu = 40;
        while (kamu <= 50) {
            System.out.println("angka = " + kamu);
            kamu++;
        }
        int kalian = 50;
        while (kalian >= 40) {
            System.out.println("angka : " + kalian);
            kalian--;
        }
        for(int kami = 10;kami >= 1;kami--){
            System.out.println("angka " + kami);
        }
        for(int saya = 1;saya <= 10;saya++){
            System.out.println("angka : "+ saya);
        }
    }
}
